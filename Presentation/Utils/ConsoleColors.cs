namespace Presentation.Utils;

/// <summary>
/// Classe estática para definir constantes de formatação de cores para a saída no console.
/// </summary>
public static class ConsoleColors
{
    /// <summary>
    /// Resetar a formatação da cor no console.
    /// </summary>
    public const string Reset = "\u001b[0m";

    /// <summary>
    /// Cor preta para a saída no console.
    /// </summary>
    public const string Black = "\u001b[30m";

    /// <summary>
    /// Cor vermelha para a saída no console.
    /// </summary>
    public const string Red = "\u001b[31m";

    /// <summary>
    /// Cor verde para a saída no console.
    /// </summary>
    public const string Green = "\u001b[32m";

    /// <summary>
    /// Cor amarela para a saída no console.
    /// </summary>
    public const string Yellow = "\u001b[33m";

    /// <summary>
    /// Cor azul para a saída no console.
    /// </summary>
    public const string Blue = "\u001b[34m";

    /// <summary>
    /// Cor magenta para a saída no console.
    /// </summary>
    public const string Magenta = "\u001b[35m";

    /// <summary>
    /// Cor ciano para a saída no console.
    /// </summary>
    public const string Cyan = "\u001b[36m";

    /// <summary>
    /// Cor branca para a saída no console.
    /// </summary>
    public const string White = "\u001b[37m";
}

